package strings;

public class Java {
    public static void main(String[] args) {
        int Number1 = 10;
        float Float = 220F;
        double Number2 = 134.5;

        byte Byte = 10;

        Double number = Number1 + Float + Number2 + Byte;
        Double number2 = Number1 * Float * Number2 * Byte;
        Float number3 = Byte * Float * Number1;
        System.out.println(number);
        System.out.println(number2);
        System.out.println(number3);



    }
}
